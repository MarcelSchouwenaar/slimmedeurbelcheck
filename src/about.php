<h1>Over Check met een Goed Gesprek</h1>
<p>"Check met een Goed Gesprek" is een initiatief dat buren helpt om afspraken te maken over het gebruik van slimme deurbelcamera's in hun buurt. Het doel is om een veilige en transparante omgeving te creëren waarin buren zich veilig en gerespecteerd voelen.</p>
<p>Door samen een formulier in te vullen, kunnen buren bevestigen dat ze het eens zijn over de installatie van een slimme deurbel. Na bevestiging via e-mail ontvangen zij een sticker die zij naast hun deurbel kunnen plaatsen.</p>
<p>Dit initiatief is opgezet door het Consortium Slimme Deurbellen, een samenwerking van verschillende organisaties die zich inzetten voor privacy en veiligheid in de digitale wereld.</p>
